import java.util.Scanner;
class StringsSum
{
	public static void main(String[] args) 
	{	Scanner os=new Scanner(System.in);
		System.out.print("Enter a num:");
		int n=os.nextInt();
		int i=1;
		int	sum=0;
		String s="";
		for (;i<=n ;i++ ){
			sum+=i;
			if (i==n)
				s+=i;
			if (i<n)
			s+=i+"+";

		}
			
			System.out.println(sum);
			System.out.println(s);
			System.out.println(s+ "= "+sum);
	}
}